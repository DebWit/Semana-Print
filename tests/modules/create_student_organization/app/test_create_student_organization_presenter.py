import json


from src.modules.create_student_organization.app.create_student_organization_presenter import lambda_handler

class Test_CreateStudentOrganizationPresenter:

    def test_create_student_organization_presenter(self):
        event = {
            "version": "2.0",
            "routeKey": "$default",
            "rawPath": "/my/path",
            "rawQueryString": "parameter1=value1&parameter1=value2&parameter2=value",
            "cookies": [
                "cookie1",
                "cookie2"
            ],
            "headers": {
                "header1": "value1",
                "header2": "value1,value2"
            },
            "queryStringParameters": {
                "name": "Student Organization",
                "description": "Student Organization Description",
                "creation_date": 1234567890,
                "logo": "Logo",
                "instagram": "Instagram",
                "website_link": "Website Link"
            },
            "requestContext": {
                "accountId": "123456789012",
                "apiId": "<urlid>",
                "authentication": None,
                "authorizer": {
                    "iam": {
                        "accessKey": "AKIA...",
                        "accountId": "111122223333",
                        "callerId": "AIDA...",
                        "cognitoIdentity": None,
                        "principalOrgId": None,
                        "userArn": "arn:aws:iam::111122223333:user/example-user",
                        "userId": "AIDA..."
                    }
                },
                "domainName": "<url-id>.lambda-url.us-west-2.on.aws",
                "domainPrefix": "<url-id>",
                "external_interfaces": {
                    "method": "POST",
                    "path": "/my/path",
                    "protocol": "HTTP/1.1",
                    "sourceIp": "123.123.123.123",
                    "userAgent": "agent"
                },
                "requestId": "id",
                "routeKey": "$default",
                "stage": "$default",
                "time": "12/Mar/2020:19:03:58 +0000",
                "timeEpoch": 1583348638390
            },
            "body": "Hello from client!",
            "pathParameters": None,
            "isBase64Encoded": None,
            "stageVariables": None
        }

        response = lambda_handler(event, None)
        assert response["statusCode"] == 201
        assert json.loads(response["body"])["name"] == "Student Organization"
        assert json.loads(response["body"])["description"] == "Student Organization Description"
        assert json.loads(response["body"])["creation_date"] == 1234567890
        assert json.loads(response["body"])["logo"] == "Logo"
        assert json.loads(response["body"])["instagram"] == "Instagram"
        assert json.loads(response["body"])["website_link"] == "Website Link"

